    public LoginAndRegisterFrame() {
        initComponents();
        home = new HomeFrame();
        chosingFrame = new ChosingFrame();
        EventLogin event = new EventLogin(){
            @Override
            public void loginDone(){
                main.removeAll();
                main.add(chosingFrame);
                main.revalidate();
                main.repaint();
            }
                
            @Override
            public void logOut(){
                main.removeAll();
                main.add(loginAndRegister1);
                main.revalidate();
                main.repaint();
            }

            @Override
            public void toCustomFrame(int n) {
                if(n == 1){
                    home.setGender(1);
                }else home.setGender(2);
                main.removeAll();
                main.add(home);
                main.revalidate();
                main.repaint();
            }

            @Override
            public void toChosingFrame() {
                main.removeAll();
                main.add(chosingFrame);
                main.revalidate();
                main.repaint();
            }
        };
        loginAndRegister1.setEventLogin(event);
        chosingFrame.setEventLogin(event);
        home.setEventLogin(event);
    }

